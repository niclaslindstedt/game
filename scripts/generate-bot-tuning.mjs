#!/usr/bin/env node
// SPDX-License-Identifier: PolyForm-Noncommercial-1.0.0
// The BOT TUNING pipeline (see the `bot-improvement` skill). Compiles the
// hand-authored `content/bot.yaml` — the autopilot's positioning knobs,
// a global `default` layer plus per-level overrides — into
// `src/generated/botTuning.ts`, the `BOT_TUNING_OVERRIDES` object
// `src/game/bot.ts` resolves per level via `resolveBotTuning()`. It:
//   1. loads the engine's `BOT_TUNING_DEFAULTS` (the valid knob schema) and the
//      level catalog (the valid level ids) for cross-ref checks,
//   2. parses + validates bot.yaml (an unknown knob key or level id fails the
//      build, same as a bad level id in generate-levels.mjs),
//   3. writes src/generated/botTuning.ts.
// The output is gitignored and regenerated on every build (like levels.ts), so
// the YAML is the single source of truth.

import { readFileSync, mkdirSync, writeFileSync } from "node:fs";
import { register } from "node:module";
import { fileURLToPath } from "node:url";

import { parse } from "yaml";

// Engine modules under src/lib use the @game/lib alias at runtime — map it so
// the engine imports resolve cleanly under plain node.
register("./game-alias-loader.mjs", import.meta.url);

import { loadLevels } from "./level-data/load-yaml.mjs";

const engine = (p) => fileURLToPath(new URL(`../${p}`, import.meta.url));
const botYamlPath = fileURLToPath(
  new URL("../content/bot.yaml", import.meta.url),
);

// The engine defaults define the valid knob SCHEMA — never edit both by hand.
const { BOT_TUNING_DEFAULTS } = await import(engine("src/game/bot-tuning.ts"));

const FLAT_KEYS = new Set(
  Object.keys(BOT_TUNING_DEFAULTS).filter((k) => k !== "postures"),
);
const POSTURE_NAMES = new Set(Object.keys(BOT_TUNING_DEFAULTS.postures));
const POSTURE_FIELDS = new Set(Object.keys(BOT_TUNING_DEFAULTS.postures.aggro));

const validIds = new Set(loadLevels().entries.map((e) => e.id));

const errors = [];

/** Validate one PARTIAL tuning layer (a `default:` or a `levels.<id>:` block). */
function validateLayer(layer, where) {
  if (layer === null || layer === undefined) return;
  if (typeof layer !== "object" || Array.isArray(layer)) {
    errors.push(`${where}: expected a mapping of knobs`);
    return;
  }
  for (const [key, value] of Object.entries(layer)) {
    if (key === "postures") {
      if (typeof value !== "object" || value === null || Array.isArray(value)) {
        errors.push(`${where}.postures: expected a mapping of posture rows`);
        continue;
      }
      for (const [posture, row] of Object.entries(value)) {
        if (!POSTURE_NAMES.has(posture)) {
          errors.push(
            `${where}.postures: unknown posture "${posture}" ` +
              `(valid: ${[...POSTURE_NAMES].join(", ")})`,
          );
          continue;
        }
        if (typeof row !== "object" || row === null || Array.isArray(row)) {
          errors.push(`${where}.postures.${posture}: expected a mapping`);
          continue;
        }
        for (const [field, num] of Object.entries(row)) {
          if (!POSTURE_FIELDS.has(field))
            errors.push(
              `${where}.postures.${posture}: unknown field "${field}" ` +
                `(valid: ${[...POSTURE_FIELDS].join(", ")})`,
            );
          else if (typeof num !== "number" || !Number.isFinite(num))
            errors.push(
              `${where}.postures.${posture}.${field}: expected a finite number`,
            );
        }
      }
      continue;
    }
    if (!FLAT_KEYS.has(key)) {
      errors.push(
        `${where}: unknown knob "${key}" (valid: ${[...FLAT_KEYS].join(", ")})`,
      );
      continue;
    }
    if (typeof value !== "number" || !Number.isFinite(value))
      errors.push(`${where}.${key}: expected a finite number`);
  }
}

const doc = parse(readFileSync(botYamlPath, "utf8")) ?? {};
const defaultLayer = doc.default ?? {};
const levels = doc.levels ?? {};

validateLayer(defaultLayer, "default");
if (typeof levels !== "object" || levels === null || Array.isArray(levels)) {
  errors.push("levels: expected a mapping of level id → overrides");
} else {
  for (const [id, layer] of Object.entries(levels)) {
    if (!validIds.has(id))
      errors.push(`levels: unknown level id "${id}" — not a defined level`);
    validateLayer(layer, `levels.${id}`);
  }
}

if (errors.length > 0) {
  console.error(
    `${errors.length} bot.yaml error(s):\n  ${errors.join("\n  ")}`,
  );
  process.exit(1);
}

const overrides = { default: defaultLayer, byLevel: levels };

const banner = `// @generated by scripts/generate-bot-tuning.mjs — DO NOT EDIT.
// Source of truth: content/bot.yaml. Regenerate with
// \`npm run levels\` (also runs inside \`npm run assets\` / \`make assets\`).
// SPDX-License-Identifier: PolyForm-Noncommercial-1.0.0`;

const out = `${banner}
import type { BotTuningOverrides } from "../game/bot-tuning.ts";

export const BOT_TUNING_OVERRIDES: BotTuningOverrides = ${JSON.stringify(
  overrides,
  null,
  2,
)};
`;

const destDir = engine("src/generated");
mkdirSync(destDir, { recursive: true });
writeFileSync(`${destDir}/botTuning.ts`, out);
const levelCount = Object.keys(levels).length;
console.log(
  `wrote src/generated/botTuning.ts — ${Object.keys(defaultLayer).length} ` +
    `default knob(s), ${levelCount} level override(s)`,
);
