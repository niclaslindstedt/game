#!/usr/bin/env node
// SPDX-License-Identifier: PolyForm-Noncommercial-1.0.0
// LEVEL-CURVE GENERATOR: compiles `content/leveling.yaml` — the hand-authored
// XP-per-level table (the single source of truth for the hero level curve) —
// into `src/generated/leveling.ts` (gitignored, regenerated on build), which
// the engine's `xpToLevelUp` (src/game/leveling.ts) reads. Validates that the
// table covers exactly levels 1..maxLevel−1 with positive finite values, so a
// gap or typo fails the build instead of silently walling a level.
//
//   node scripts/generate-leveling.mjs
//
// Runs first in the `make levels` chain — it imports only the leveling config
// (for `maxLevel`), never the content catalogs.
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { register } from "node:module";

register("./game-alias-loader.mjs", import.meta.url);

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const { LEVELING } = await import(join(root, "src/game/config/leveling.ts"));

const src = readFileSync(join(root, "content/leveling.yaml"), "utf8");

// The file is a few top-level scalars (the flat mob-priced XP payouts) plus a
// flat `xpToNext:` map of `level: xp` rows — parse it directly (no YAML
// dependency needed for scalars, matching the other loaders' spirit).
const TUNING_KEYS = [
  "arrowXpKills",
  "eliteXpMobMult",
  "bossXpMobMult",
  "arrowDropShare",
];
const tuning = {};
const table = new Map();
let inTable = false;
for (const [lineNo, raw] of src.split("\n").entries()) {
  const line = raw.replace(/#.*$/, "").trimEnd();
  if (!line.trim()) continue;
  if (/^xpToNext:\s*$/.test(line)) {
    inTable = true;
    continue;
  }
  if (!inTable) {
    const s = line.match(/^([A-Za-z]\w*):\s*(\d+(?:\.\d+)?)\s*$/);
    if (s && TUNING_KEYS.includes(s[1])) {
      tuning[s[1]] = Number(s[2]);
      // Zero is a legal off-switch (no arrow drops / no payout); negatives
      // are typos.
      if (!Number.isFinite(tuning[s[1]]) || tuning[s[1]] < 0) {
        throw new Error(
          `content/leveling.yaml:${lineNo + 1}: ${s[1]} must be >= 0, got ${s[2]}`,
        );
      }
    }
    continue;
  }
  const m = line.match(/^\s+(\d+):\s*(\d+(?:\.\d+)?)\s*$/);
  if (!m) {
    throw new Error(
      `content/leveling.yaml:${lineNo + 1}: expected "<level>: <xp>", got: ${raw}`,
    );
  }
  const level = Number(m[1]);
  const xp = Number(m[2]);
  if (table.has(level)) {
    throw new Error(`content/leveling.yaml: duplicate level ${level}`);
  }
  if (!Number.isFinite(xp) || xp <= 0) {
    throw new Error(
      `content/leveling.yaml: level ${level} has invalid xp ${xp}`,
    );
  }
  table.set(level, xp);
}

const last = LEVELING.maxLevel - 1;
const missing = [];
for (let l = 1; l <= last; l++) if (!table.has(l)) missing.push(l);
if (missing.length) {
  throw new Error(
    `content/leveling.yaml: missing xpToNext for level(s) ${missing.join(", ")} (need 1..${last})`,
  );
}
const extras = [...table.keys()].filter((l) => l < 1 || l > last);
if (extras.length) {
  throw new Error(
    `content/leveling.yaml: xpToNext for out-of-range level(s) ${extras.join(", ")} (cap is ${LEVELING.maxLevel})`,
  );
}

const missingKeys = TUNING_KEYS.filter((k) => !(k in tuning));
if (missingKeys.length) {
  throw new Error(
    `content/leveling.yaml: missing payout scalar(s) ${missingKeys.join(", ")}`,
  );
}

const values = [];
for (let l = 1; l <= last; l++) values.push(Math.round(table.get(l)));

const out = `// SPDX-License-Identifier: PolyForm-Noncommercial-1.0.0
// GENERATED by scripts/generate-leveling.mjs from content/leveling.yaml.
// Do not edit or commit — the YAML is the source of truth.

/** XP needed to cross OUT of level L (1-based: index L−1), levels 1..${last}. */
export const XP_TO_NEXT: readonly number[] = ${JSON.stringify(values)};

/** The flat mob-priced XP payouts (regular-mob units — see the YAML header):
 * a golden arrow pays \`arrowXpKills\` reference-mob kills; an elite/boss pays
 * \`eliteXpMobMult\`/\`bossXpMobMult\` × its own mob-level XP. \`arrowDropShare\`
 * is the arrow slice of the drop ladder (per-rung \`arrowDropMult\` on top). */
export const XP_TUNING = {
  arrowXpKills: ${tuning.arrowXpKills},
  eliteXpMobMult: ${tuning.eliteXpMobMult},
  bossXpMobMult: ${tuning.bossXpMobMult},
  arrowDropShare: ${tuning.arrowDropShare},
} as const;
`;

const outPath = join(root, "src/generated/leveling.ts");
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out);
console.log(
  `wrote src/generated/leveling.ts — ${values.length} levels (L1 ${values[0]} xp … L${last} ${values[last - 1]} xp)`,
);
